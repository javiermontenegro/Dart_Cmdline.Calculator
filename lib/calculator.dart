library calculator;

class Calculator{
    double sum(double x, double y){
        return x + y;
    }//End sum

    double substract(double x, double y){
        return x - y;
    }//End substract

    double multiply(double x, double y){
        return x * y;
    }//End multiply
  
    double divide(double x, double y){
        return x / y;
    }//End divide
}//End Calculator